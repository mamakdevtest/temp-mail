export function normalizeLanguage(language) {
  return language === 'en' ? 'en' : 'tr';
}

function resolvePath(source, key) {
  return key.split('.').reduce((acc, part) => (acc && Object.prototype.hasOwnProperty.call(acc, part) ? acc[part] : undefined), source);
}

export function createTranslator(language) {
  const lang = normalizeLanguage(language);
  return (key, vars = {}) => {
    const fallback = resolvePath(TRANSLATIONS.tr, key);
    const value = resolvePath(TRANSLATIONS[lang], key) ?? fallback ?? key;
    if (typeof value !== 'string') return key;
    return value.replace(/\{(\w+)\}/g, (_, token) => (vars[token] ?? ''));
  };
}

const LocaleContext = createContext({
  language: 'tr',
  t: (key) => key,
});

export function LocaleProvider({ language, children }) {
  const value = useMemo(() => {
    const normalized = normalizeLanguage(language);
    return {
      language: normalized,
      t: createTranslator(normalized),
    };
  }, [language]);

  return createElement(LocaleContext.Provider, { value }, children);
}

export function useLocale() {
  return useContext(LocaleContext);
}
